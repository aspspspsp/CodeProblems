package Medium;
/*	2M制		1的	解
 * -----------------------------------
 *0		0			0		第0Y果
 *1		1			1		第0Y果+1
 * -----------------------------------
 *2		10			1 		第0Y果+1
 *3		11			2 		第1Y果+1
 * -----------------------------------
 *4		100			1		第0Y果+1
 *5 	101			2 		第1Y果+1
 *6 	110			2		第2Y果+1
 *7	 	111			3		第3Y果+1
 * -----------------------------------
 *8 	1000		1		第0Y果+1	
 *9 	1001		2		第1Y果+1
 *10 	1010		2		第2Y果+1
 *11 	1011		3		第3Y果+1
 *12	1100		2		第4Y果+1
 *13	1101		3		第5Y果+1
 *14 	1110		3		第6Y果+1
 *15 	1111		4		第7Y果+1
*/
public class CountingBits {
	public int[] countBits(int num) {
		int[] result = new int[num + 1];
		
		int powOf2 = 1; //zy(是否可被2整除的)一_始2的0次方
		int before = 1; //此量是用泶嫒∏懊娼Y果(1的)
		for(int i = 1; i <= num; i ++) {
			if(i == powOf2) { //若此2的次方
				result[i] = 1; //此悼杀2整除r，只有一1
				powOf2 <<= 1; //zy涤乙埔晃(乘2的效果)
				
				before = 1; //重置，表示1的依前面的Y果而定
			} else { //否t
				result[i] = result[before] + 1; //1的凳歉前面1的 + 1
				before = before + 1; //⒋俗量往前挪
			}
		}
	    return result;
	}
}
